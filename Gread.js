// >= 90 => A
// 80 - 89 => B
// 60 - 79 => C
// 33 - 59 => D
// < 33 => F

let marks = 90;

if(marks >= 90)
  console.log("Grade A");

else if(marks >= 80)
  console.log("Grade B");

else if(marks >= 60) 
  console.log("Grade C");

else if(marks >= 33) 
  console.log("Grade D");

else 
  console.log("Grade F")


